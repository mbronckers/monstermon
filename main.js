window.onload = function() {
    canvas = document.getElementById("game");
    ctx = canvas.getContext("2d");

    drawMap();
}

function drawMap() {
    for (var r = 0; r < map.rows; r++) {
        for (var c = 0; c < map.cols; c++) {
            var tile = map.getTile(r, c);
            
            if (tile !== -1) { // a value of -1 in the map array means empty tile
                ctx.drawImage(
                    texture,
                    (tile) * map.tileSize, // source x position --> texture file tile's x position
                    (c * map.tileSize),                  // source y position --> texture file tile's y position
                    map.tileSize, 		// source tile width
                    map.tileSize,		// source tile height
                    c * map.tileSize, 	// x position of tile on map
                    r * map.tileSize,	// y position of tile on map
                    map.tileSize,		// tile width
                    map.tileSize		// tile height
                );
            }
        }
    }
}


